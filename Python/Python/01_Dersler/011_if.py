ocak = 1
aralik = 12

if ocak > aralik:
    print("ocak aralıktan büyüktür")

if ocak < aralik:
    print("ocak aralıktan küçüktür")

if ocak >= aralik:
    print("ocak aralığa büyük yada eşittir")

if ocak <= aralik:
    print("ocak aralığa küçük yada eşittir")

if ocak == aralik:
    print("ocak aralığa eşittir")

if ocak != aralik:
    print("ocak aralığa eşit değildir")